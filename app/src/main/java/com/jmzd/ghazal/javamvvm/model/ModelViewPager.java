package com.jmzd.ghazal.javamvvm.model;

public class ModelViewPager {
    String name ;
    String image ;



    public ModelViewPager(String name, String image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }


}
